package logic;

public class LogicCalculator {

	public double result;
	
	public void sum (double a, double b){
		result = a+b;
	}

	public void sub (double a, double b){
		result = a-b;
	}
	
	public void div (double a, double b){
		result = a/b;
	}
	
	public void mul (double a, double b){
		result = a*b;
	}
	
}
